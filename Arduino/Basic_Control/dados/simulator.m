Vi = 3.535;
Vf = 3.785;

t = 0:0.0001:2;

% p1 = 0.008391063263791;
% p2 = 0.029646566281119;
% p3 = 0.021355097710248;


p1 = 0.000839106326379128;
p2 = 0.00296465662811186;
p3 = 0.00213550977102477;


tau = p1*(Vf-Vi)^2 + p2*(Vf-Vi)+p3;

ti = 0.5;

for i = 1:length(t)
    if t(i) >= ti
      v(i) = Vf - (Vf - Vi)*exp((-t(i)-ti)/tau);
    else
        v(i) = Vi;
    end
end




plot(t, v);
